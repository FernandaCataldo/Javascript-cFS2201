//FOR LOOPS

for(let i = 0; i<3; i++){
    console.log(i)
}

//PRIMER VUELTA
//1.1) i es igual a 0
//1.2) compara el valor de i en la condicional: 0 < 3 
//1.3) La condicional aun es true
//1.4) Ejecuta la accion: console.log(0)
//1.5) Ejecuta el increment: i++ ---> i ahora es igual a 1

//SEUGUNDA VUELTA 
//2.1) i es igual 1
//2.2) compara el valor de i en la condicional: 1 < 3 
//2.3) La condicional aun es true 
//2.4) Ejecuta la accion: console.log(1)
//2.5) Ejecuta el increment: i++ ---> i ahora es igual a 2

//TERCERA VUELTA 
//3.1) i es igual 2
//3.2) compara el valor de i en la condicional: 2 < 3 
//3.3) La condicional aun es true 
//2.4) Ejecuta la accion: console.log(2)
//3.5) Ejecuta el increment: i++ ---> i ahora es igual a 3

//FIN
//4.1) i es igual a 3
//4.2) compara el valor de i en la condicional: 3 < 3 
//4.3) la condicional se hizo false ---> FIN DEL LOOP

//FOR LOOP DECREMENTO
for(let i = 100; i>0; i--){
    console.log(i)
}

// SIEMPRE ES IMPORTANTE CHEQUEAR QUE LA CONDICIONAL SE VUELVA FALSA EN ALGUN MOMENTO PARA EVITAR LOOP INFINITOS */